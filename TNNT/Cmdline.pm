#!/usr/bin/env perl

#=============================================================================
# Processing of the command-line options.
#=============================================================================

package TNNT::Cmdline;

use Moo;
with 'MooX::Singleton';

use Getopt::Long;



#=============================================================================
#=== ATTRIBUTES ==============================================================
#=============================================================================


# --debug

has debug => (
  is => 'rwp',
);



#=============================================================================
# Initialize the object according to the command-line options given
#=============================================================================

sub BUILD {
  my ($self, $args) = @_;

  if(!GetOptions(
    'debug' => sub { $self->_set_debug(1); }
  )) {
    die 'Invalid command-line argument';
  }
};



#=============================================================================

1;
