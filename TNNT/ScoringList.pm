#!/usr/bin/env perl

#=============================================================================
# Scoring list for players/clans.
#=============================================================================

package TNNT::ScoringList;

use Moo::Role;



#=============================================================================
#=== ATTRIBUTES ==============================================================
#=============================================================================

has scores => (
  is => 'ro',
  default => sub { [] },
);



#=============================================================================
#=== METHODS =================================================================
#=============================================================================

sub add_score
{
  my (
    $self,
    $entry,
  ) = @_;

  my $lst = $self->scores();
  push(@$lst, $entry);
}



#=============================================================================

1;
