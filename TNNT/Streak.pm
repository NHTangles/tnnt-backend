with 'TNNT::GameList::AddGame';



#=============================================================================
#=== METHODS =================================================================
#=============================================================================

sub disp
{
  my ($self) = @_;

  printf "--- STREAK START (player=%s, len=%d)---\n",
    $self->last_game()->name(), $self->count_games();
  $self->iter_games(sub {
    $_->disp();
  });
  print "--- STREAK END ---\n";
}



